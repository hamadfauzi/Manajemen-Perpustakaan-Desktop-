
package Model;


public class Denda {
	private double Nominal;
	private String Keterangan;
	public void setNominal(double nominal) {
		Nominal = nominal;
	}
	public void setKeterangan(String keterangan) {
		Keterangan = keterangan;
	}
	public double getNominal() {
		return Nominal;
	}
	public String getKeterangan() {
		return Keterangan;
	}
}
